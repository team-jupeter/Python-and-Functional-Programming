# 5.1.3 Working with the parts of a string
fruit = "banana"
letter = fruit[1]
print(letter) # a

letter = fruit[0]
print(letter) # b

fruit = "banana"
list(enumerate(fruit)) # [(0, 'b'), (1, 'a'), (2, 'n'), (3, 'a'), (4, 'n'), (5, 'a')]

