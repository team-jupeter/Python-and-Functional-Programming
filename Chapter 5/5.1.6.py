# 5.1.6 Slices
phrase = "Pirates of the Caribbean"
print(phrase[0:7]) # Pirates
print(phrase[11:14]) # the
print(phrase[13:24]) # e Caribbean
friends = ["Joe", "Zoe", "Brad", "Angelina", "Zuki", "Thandi", "Paris"]
print(friends[2:4]) # ['Brad', 'Angelina']

word = "banana"
word[:3] # 'ban'
word[3:] # 'ana'
word[3:999] # 'ana'